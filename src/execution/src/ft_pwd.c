#include "../../../minishell.h"

int	ft_pwd(void)
{
	char	path[4096];

	if (getcwd(path, 4096) != NULL)
	{
		ft_putstr_fd (path, 1);
		write (1, "\n", 1);
		return (0);
	}
	return (0);
}
